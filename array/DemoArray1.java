class DemoArray1
{
	public static void main(String args[])
	{	
		int arr[]={11,22,33,44,5,8,86,6888,6788,87998,65,6,6,66,77};
		int len=arr.length ;
		System.out.println("length is :"+len);
		for (int i=0; i<len;i++)
		{
			System.out.println("Array [" + i + "] is:"+arr[i]);
		}
		System.out.println("End of program");
	}
}